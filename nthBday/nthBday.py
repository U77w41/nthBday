def second_sat(month : int, year : int) -> date:
    """
    Returns the Second Saturday of the month 
    
    Parameters:
    month: month in int format
    year: year in int format
    
    
    # Example:
    
    import nthBday as nb
    print(nb.sat_sec(2,2023))
    
    >>> 2023-02-11

    """  
    dt=date(year,month,1) # first day of month
    first_w=dt.isoweekday()  # weekday of 1st day of the month
    if(first_w==7): # if it is Sunday return 0 
	    first_w=0
     
    second_saturday_ = 14-first_w
    
    return date(year,month,second_saturday_)



